"""Scenario layer for social-jira3: jira2 generation reused verbatim + inbox attachment.

The pairing instances (roster, tasks, role-driven goodness, G*-set, one-dislike feelings,
resolvable/conflict invariants) are generated by ``social_jira2.scenario`` — imported, not
copied, so the same seeds produce byte-identical instances in both experiments
(scenario-level comparability, SPEC §8). jira2 is frozen, so there is no drift risk.

What v3 adds is a **presentation layer**: :func:`attach_inboxes` turns the feelings ground
truth into per-employee message inboxes on a separate seed stream (SPEC §2.4). It never
touches the generation math — reparameterizing the inbox cannot shift ``G*`` or the type.
"""

